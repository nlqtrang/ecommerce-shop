    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        //processRequest(request, response);
        String deleteid = request.getParameter("deleteid");
        if (deleteid!=null && !deleteid.isEmpty()) {
        new ProductsCategoriesDAO().deleteTypes(Integer.parseInt(deleteid));
        }
        String updateid = request.getParameter("updateid");
        if (updateid!=null && !updateid.isEmpty()) {
            String name = request.getParameter("newValue");
            new ProductsCategoriesDAO().updateTypes(Integer.parseInt(updateid), name);
        }
        response.sendRedirect("products-manager");
    } 

